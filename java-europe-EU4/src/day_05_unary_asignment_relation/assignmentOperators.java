package day_05_unary_asignment_relation;

public class assignmentOperators {

	public static void main(String[] args) {
		
		int i=100;
		System.out.println(i);
		
		i+=1;
		System.out.println(i);
		
		i-=2;
		System.out.println(i);
		
		i*=3;
		System.out.println(i);
		
		i/=4;
		System.out.println(i);
		
		i%=5;
		System.out.println(i);
		

	}

}
