public class task29_BiggerNumbers {

	public static void main(String[] args) {
		
		int x,y;
		x=10;
		y=10;
		
		if(x>y) {
			System.out.println(x + " number is greater than " + y+ " number");
		}
		if(x<y) {
			System.out.println(y + " number is greater than " + x+ " number");
		}else {
			System.out.println("The numbers are equal");
		}

	}

}
